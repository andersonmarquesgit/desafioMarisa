package com.desafiomarisa;

public class FizzBuzz {

	private int num;
	private String desc;
	
	public FizzBuzz(int num, String desc) {
		this.num = num;
		this.desc = desc;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

}
